<h1>Local Business?</h1>
<p>We have defined the term 'local' to mean any business owned and operated within the Lancaster City Council boundaries where the owner also lives within the Lancaster City Council boundaries.</p>
<p>If this is you, then please sign up to the become a PEAR TRADING business.</p>
<h1>Local Customers</h1>
<p>Feel free to sign up and spread the word with your friends and businesses you shop with.</p>
